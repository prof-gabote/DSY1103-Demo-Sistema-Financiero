CREATE TABLE IF NOT EXISTS bd_boletas.proveedor (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 100 INCREMENT BY 1) PRIMARY KEY,
    rut VARCHAR(10) NOT NULL UNIQUE,
    nombre VARCHAR(100) NOT NULL,
    giro VARCHAR(100),
    direccion VARCHAR(200),
    telefono VARCHAR(20)
);

ALTER TABLE IF EXISTS bd_boletas.boleta
ALTER COLUMN id 
    SET GENERATED BY DEFAULT
    SET START WITH 100
    SET INCREMENT BY 1,
ALTER COLUMN tipo SET DATA TYPE VARCHAR(10),
DROP COLUMN rut_proveedor,
ADD COLUMN id_proveedor BIGINT,
ADD CONSTRAINT fk_proveedor FOREIGN KEY (id_proveedor) REFERENCES bd_boletas.proveedor(id);


